    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store()
    {

        Message::create(
          [
            'chain_id'      =>  request()->chain_id,
            'subject'       =>  request()->subject,
            'body'          =>  request()->body,
            'days'          =>  request()->days,
            'time'          =>  request()->time
          ]);
        return back();
    }

